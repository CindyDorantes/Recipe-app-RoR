require 'rails_helper'

RSpec.describe User, type: :model do
  # tests go here
  before :each do
    @user = User.create(
      name: 'Microverse',
      email: 'd@gmail.com',
      password: 'password'
    )
  end

  context 'Test user validations' do
    it 'is valid with valid attributes' do
      expect(@user).to be_valid
    end

    it 'name must not be blank' do
      @user.name = nil
      expect(@user).to_not be_valid
    end
  end
end
